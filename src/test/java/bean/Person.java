package bean;

public class Person {

	private String name;
	
	private int age;
	
	private String LIKE;
	
	private String INTE;

	public String getLIKE() {
		return LIKE;
	}

	public void setLIKE(String lIKE) {
		LIKE = lIKE;
	}

	public String getINTE() {
		return INTE;
	}

	public void setINTE(String iNTE) {
		INTE = iNTE;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
